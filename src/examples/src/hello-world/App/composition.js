import { ref } from 'vue'

export default {
  setup() {
    // است reactive یک منبع داده "ref"
    // که یک مقدار را ذخیره می‌کند
    // از نظر فنی، نیازی نیست که رشته را 
    // بپیچیم ref() با استفاده از
    // به منظور نمایش آن، اما در ادامه خواهیم دید 
    // که چرا این کار لازم است 
    // اگر قصد داشته باشیم مقدار آن را تغییر دهیم
    const message = ref('!سلام دنیا')

    return {
      message
    }
  }
}
